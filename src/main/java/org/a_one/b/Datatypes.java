package org.a_one.b;
   
    
public class Datatypes {
	public static void main(String[] args) {
		// General variables
	byte age=25;
	System.out.println("Muthu name is "+ age);
	
	short bikenumber=1995;
	System.out.println("bike Number is "+ bikenumber);
	
	int password=123456;
	System.out.println("secret number is "+ password);
	
	long contact=1234567891L;
	System.out.println("phone number is "+ contact);
	
	System.out.println("-------------------------------------------------------------------");
	
	//Decimal digits
	
	float salary=340.59f;
	System.out.println("daily income "+ salary);
	
	double netvalue=1.23456789199;
	System.out.println("payslip "+ netvalue);
	
	System.out.println("-------------------------------------------------------------------");
	
	//Characters
	
	char M='k'; //only one letter
	System.out.println("only one "+M);
	
	String N="kumar";
	System.out.println("that name "+N);
	
    // boolian
	
	boolean s=true;
	System.out.println("varum paru "+s);
}}
