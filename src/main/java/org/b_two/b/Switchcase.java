package org.b_two.b;

public class Switchcase {
public static void main(String[] args) {
	
	char day='s';
	
  switch (day) 
	{
	case 'a':
	System.out.println("sunday");
	break;
	
	case 'b':
	System.out.println("monday");
	break;
	
	case 's':
	System.out.println("tuesday");
	break;
	
	}  // use "break"  
	
	
}}
